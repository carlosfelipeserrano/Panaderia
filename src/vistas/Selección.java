    private void txtrutsKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtrutsKeyTyped
        // TODO add your handling code here:
        char validar = evt.getKeyChar();

        if (!Character.isDigit(validar) && Character.compare(validar, 'k') != 0) {

            evt.consume();
            return;

        }
        if (txtruts.getText().length() >= 12) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Sólo se debe ingresar como máximo 12 caracteres");
            return;
        }

        if (txtruts.getText().length() == 2 || txtruts.getText().length() == 6) {
            txtruts.setText(txtruts.getText() + '.');
        } else if (txtruts.getText().length() == 10) {
            txtruts.setText(txtruts.getText() + '-');
        }
    }//GEN-LAST:event_txtrutsKeyTyped

