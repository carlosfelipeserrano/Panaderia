    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
      if (txtpan1.getText().isEmpty()||txtpan2.getText().isEmpty()||txtpan3.getText().isEmpty()||txtpan4.getText().isEmpty()||txtpan5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Si el Cliente no compro kilogramos deje el campo con el digito 0", "AVISO", JOptionPane.INFORMATION_MESSAGE);
        }
        Date fecha = new Date();

        int Hallulla = Integer.parseInt(txtpan1.getText());
        int Marraqueta = Integer.parseInt(txtpan2.getText());
        int Colisa = Integer.parseInt(txtpan3.getText());
        int Molde = Integer.parseInt(txtpan4.getText());
        int Amasado = Integer.parseInt(txtpan5.getText());
        String kilos = t1.getText();
        String precio = tprecio.getText();
        int idCliente = ClienteData.getInstance().getIdCliente();
         int idPanes = PanesData.getInstance().getIdPanes();
         if (t1.getText().isEmpty() || tprecio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Antes de Guardar la información, debe calcular los kilogramos.","AVISO", JOptionPane.INFORMATION_MESSAGE);
        }else{
              try {

            consultas.guardarPan(fecha, kilos, precio, idCliente, idPanes);
            JOptionPane.showMessageDialog(null, "Datos Guardados Correctamente", "AVISO", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            System.out.println(e);
        }

             
         }

       

    }//GEN-LAST:event_loginBtnActionPerformed

