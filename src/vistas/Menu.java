    private void JPrecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JPrecioKeyTyped
        // TODO add your handling code here:
        // Lógica para formatear el texto como "0.000" cada vez que se teclea una tecla
        String text = JPrecio.getText();
        // Asegúrate de que el texto no sea demasiado largo
        if (text.length() > 4) { // limita el texto a 5 caracteres
            evt.consume(); // ignora el último carácter tecleado
            return;
        }
        // Asegúrate de que el texto sea solo números
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) {
            evt.consume(); // ignora el último carácter tecleado
            return;
        }
        // Añade un punto decimal después del primer dígito
        if (text.length() == 1) {
            JPrecio.setText(text + "." + c);
            evt.consume(); // ignora el último carácter tecleado
        }
    }//GEN-LAST:event_JPrecioKeyTyped

