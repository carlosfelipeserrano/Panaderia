    private void loginBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtn2ActionPerformed
        // TODO add your handling code here:
        Usuarios mod = new Usuarios(); // Crear una instancia de la clase Usuarios para almacenar los datos del usuario
        Consulta con = new Consulta(); // Crear una instancia de la clase Consulta para realizar consultas o actualizaciones en la base de datos

        String pass = new String(jpassadmi.getPassword()); // Obtener la contraseña ingresada como una cadena de caracteres
        String passCon = new String(jpassadmin1.getPassword()); // Obtener la confirmación de contraseña ingresada como una cadena de caracteres

        if (pass.equals(passCon)) { // Verificar si la contraseña y la confirmación coinciden

            String contraseña = Hash.md5(pass); // Encriptar la contraseña utilizando el algoritmo MD5 (supuesto método Hash.md5())

            mod.setUsuario(txtusu.getText()); // Establecer el nombre de usuario en el objeto Usuarios
            mod.setContrasena(contraseña); // Establecer la contraseña encriptada en el objeto Usuarios

            if (con.actualizardatosadmin(mod)) { // Llamar al método actualizardatos en la instancia de Consulta pasando el objeto Usuarios como parámetro, y verificar si la actualización fue exitosa
                int respuesta = JOptionPane.showConfirmDialog(null, "¿Deseas limpiar los datos?", "Confirmación", JOptionPane.YES_NO_OPTION); // Mostrar un cuadro de diálogo de confirmación
                if (respuesta == JOptionPane.YES_OPTION) { // Verificar si la opción seleccionada en el cuadro de diálogo es "Sí"
                }
            } else {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden", "AVISO", JOptionPane.INFORMATION_MESSAGE); // Mostrar un mensaje de error si las contraseñas no coinciden
            }
        }
    }//GEN-LAST:event_loginBtn2ActionPerformed

