    private void txtrutKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtrutKeyTyped
        char validar = evt.getKeyChar();
        if (!Character.isDigit(validar) && Character.compare(validar, 'k') != 0) {

            evt.consume();
            return;

        }
        if (txtrut.getText().length() >= 12) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "S√≥lo se debe ingresar como m√°ximo 12 caracteres");
            return;
        }

        if (txtrut.getText().length() == 2 || txtrut.getText().length() == 6) {
            txtrut.setText(txtrut.getText() + '.');
        } else if (txtrut.getText().length() == 10) {
            txtrut.setText(txtrut.getText() + '-');
        }
    }//GEN-LAST:event_txtrutKeyTyped

