    private void btnactualizarcliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarcliActionPerformed
        // TODO add your handling code here:
        Cliente mod = new Cliente(); // Crear una instancia de la clase Usuarios para almacenar los datos del usuario
        if (txtrut.getText().isEmpty() || txtnombre.getText().isEmpty() || txtapellido.getText().isEmpty() || txtcorreo.getText().isEmpty() || txtdireccion.getText().isEmpty() || txttelefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Rellenar Campos");
        } else {
            Consulta con = new Consulta();
            ConexionBD db = new ConexionBD();
            mod.setIdCliente(cliente.getIdCliente());

            mod.setRut(txtrut.getText());
            mod.setNombre(txtnombre.getText());
            mod.setApellido(txtapellido.getText());
            mod.setCorreo(txtcorreo.getText());
            mod.setDireccion(txtdireccion.getText());
            mod.setTelefono(txttelefono.getText());
            mod.setEstado(Checkestado.isSelected() ? "true" : "false");

            try {
                db.getConexion();

                if (con.actualizarCliente(mod)) {
                    this.cliente.getIdCliente();
                    this.Checkestado.isSelected();
                    
                    this.setVisible(false);
                    VerClientes f1 = new VerClientes();
                    f1.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "ERRor");
                }

            } catch (Exception e) {
            }

        }


    }//GEN-LAST:event_btnactualizarcliActionPerformed

